package summer.book_shop.domain;

public class Selling {
    static String sell_code;
    static int sell_count;

    public String getSell_code() {
        return sell_code;
    }

    public void setSell_code(String sell_code) {
        this.sell_code = sell_code;
    }

    public int getSell_count() {
        return sell_count;
    }

    public void setSell_count(int sell_count) {
        this.sell_count = sell_count;
    }
}
